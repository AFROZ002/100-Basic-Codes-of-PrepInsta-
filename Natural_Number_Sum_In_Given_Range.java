package PrepInsta_100_Program;

public class Natural_Number_Sum_In_Given_Range {
    public static void main(String[] args) {
        int n1=12;
        int n2=15;
        int sum=0;
        for (int i=n1;i<=n2;++i){
            sum+=i;
        }
        System.out.println(sum);
    }
}
